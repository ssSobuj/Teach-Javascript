// Assignment Operators

//  what is assignment operator?
// Assignment operators are used to assign values to variables.

let x = 10; // Simple assignment
x += 5; // x = x + 5

console.log("Addition Assignment (x += 5):", x); // 15  

let y = 20; // Simple assignment
y -= 5; // y = y - 5

console.log("Subtraction Assignment (y -= 5):", y); // 15

let m = 5; // Simple assignment
m *= 2; // m =m *2
console.log("Multiplication Assignment (m *= 2):", m); // 10

let n = 20; // Simple assignment
n /= 4; // n = n / 4
console.log("Division Assignment (n /= 4):", n); // 5

let p = 10; // Simple assignment
p %= 3; // p = p % 3                    
console.log("Modulus Assignment (p %= 3):", p); // 1

